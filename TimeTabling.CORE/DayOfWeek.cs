namespace TimeTabling.CORE
{
    /// <summary>
    /// Dias de la semana que se impartira esta asignatura
    /// </summary>
    public enum DayOfWeek : int
    {
        /// <summary>
        /// Primer dia de la semana
        /// </summary>
        Lunes = 0,

        /// <summary>
        /// Segundo dia de la semana
        /// </summary>
        Martes = 1,

        /// <summary>
        /// Tercero dia de la semana
        /// </summary>
        Miercoles = 2,

        /// <summary>
        /// Cuarto dia de la semana
        /// </summary>
        Jueves = 3,

        /// <summary>
        /// Quinto dia de la semana
        /// </summary>
        Viernes = 4
    }
}